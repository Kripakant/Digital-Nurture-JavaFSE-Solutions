// Exercise 6: Data Type Demonstration
// Objective: Understand Java's primitive data types.
public class Exercise06_DataTypeDemonstration {
    public static void main(String[] args) {
        // Declare variables of different primitive types.
        int myInt = 100;
        float myFloat = 3.14f;
        double myDouble = 19.99;
        char myChar = 'A';
        boolean myBoolean = true;

        // Additional primitive types for completeness.
        byte myByte = 10;
        short myShort = 5000;
        long myLong = 123456789L;

        // Display each variable.
        System.out.println("int value     : " + myInt);
        System.out.println("float value   : " + myFloat);
        System.out.println("double value  : " + myDouble);
        System.out.println("char value    : " + myChar);
        System.out.println("boolean value : " + myBoolean);
        System.out.println("byte value    : " + myByte);
        System.out.println("short value   : " + myShort);
        System.out.println("long value    : " + myLong);
    }
}
